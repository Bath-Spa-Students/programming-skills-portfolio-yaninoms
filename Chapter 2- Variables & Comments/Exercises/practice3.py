'''Write a python program that takes an input 5 from user and then type cast those values to string, int and float in the separate variables. Print all the variables'''

# Input from user
userinput = input("Enter 5:")

# changing to string, int and float
tostring = str(userinput)
tointeger = int(userinput)
tofloat = float(userinput)

# displaying results
print(f"ORIGINAL: {userinput}") # printing the original
print(f"\nAs string: {tostring}") # as a string
print(f"\nAs an integer: {tointeger}") # as an integer
print(f"\nAs float: {tofloat}") # as a float




