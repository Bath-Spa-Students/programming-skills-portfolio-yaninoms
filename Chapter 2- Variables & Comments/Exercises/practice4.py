'''Write a python program that stores an integer and string value to variables x and y. Print the type of variable x and y.'''

# Asking for user input / storing as int and str
x = int(input("Enter 1st value:"))
y = str(input("Enter 2nd value:"))
# displaying what type of variable is x and y
print(f"1){x} : {(type(x))}")
print(f"2){y} : {(type(y))}")
