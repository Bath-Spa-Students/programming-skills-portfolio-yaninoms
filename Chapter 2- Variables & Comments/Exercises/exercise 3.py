# Exercise 3: Stripping Names

# Assigning a name to a variable
print("Unmodified:")
name="\t\n Miliani Leopoldo \n\t"
print(name)

# Print using lstrip()
print("With .lstrip():")
print (name.lstrip())

# Print using rstrip()
print("With .rstrip()")
print (name.rstrip())

# Print using strip()
print("With .strip():")
print (name.strip())

