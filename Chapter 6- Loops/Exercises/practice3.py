'''Write a Python program that uses a loop to find the sum of all the numbers from 1 to 100.'''
# starting number
total_sum = 0

for i in range(1,101): # range from 1 to 100
    total_sum += i # gradually increase by 1 to reach 100
# print the sum
print(f"SUM:{total_sum}")




