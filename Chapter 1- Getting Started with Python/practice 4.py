# Q4 Compute the Area of a triangle

# Title
print("Area of triangle")

# Input of values  
a = float(input('Enter first side: '))
b = float(input('Enter second side: '))
c = float(input('Enter third side: '))

# Computation of Area
s = (a + b + c) / 2

# Results 
print(f"The Area of the triangle is {s}")


