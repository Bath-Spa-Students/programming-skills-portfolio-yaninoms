# Exercise 4: Stages of Life

# Assigning age
age = 18

# If-elif-else conditions
if age < 2:
    print("You're still a baby.")
elif age < 4:
    print("You're still a toddler.")
elif age < 13:
    print("You're still a tween.")
elif age < 20:
    print("You're still a teenager.")
elif age > 65:
    print("You're an adult!")
else:
    print("You're an elder.")


